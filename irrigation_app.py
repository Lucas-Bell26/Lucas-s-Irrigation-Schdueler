# Ensure 'Date' and 'Year' columns exist for date reformatting
required_cols_for_date = ['Year', 'Month', 'Date']
if not all(col in df.columns for col in required_cols_for_date):
    st.error(f"Missing one or more required date columns ({required_cols_for_date}). Please check your CSV file.")
    st.stop()

# Organize and reformat the date columns for streamlit dropdown selection
df['Date_String'] = df['Year'].astype(str) + '-' + df['Month'].astype(str) + '-' + df['Date'].astype(str)
df['Date_YMD'] = pd.to_datetime(df['Date_String'], format='%Y-%m-%d', errors='coerce')

# Drop rows where Date_YMD could not be parsed
df.dropna(subset=['Date_YMD'], inplace=True)

df['Month_Name'] = df['Date_YMD'].dt.strftime('%B')
df['Day'] = df['Date_YMD'].dt.day
df.drop(['Date_String', 'Date'], axis=1, inplace=True)
if 'Time' in df.columns: # 'Time' column might not exist in all datasets
    df.drop('Time', axis=1, inplace=True)

df = df.reindex(columns=['Date_YMD', 'Day'] + [col for col in df.columns if col not in ['Date_YMD', 'Day']])

# Ensure required columns for calculations exist
required_cols_for_calc = ['Temperature_High_F', 'Temperature_Low_F', 'Precipitation_inches', 'ET_inches']
if not all(col in df.columns for col in required_cols_for_calc):
    st.error(f"Missing one or more required data columns for calculations ({required_cols_for_calc}). Please check your CSV file.")
    st.stop()

# Perform calculations
df.insert(6, 'Temp_Avg', (df['Temperature_High_F'] + df['Temperature_Low_F'])/2)
df.insert(13, 'Precip_Cum', df['Precipitation_inches'].cumsum())
df.insert(14, 'ET_Cum', df['ET_inches'].cumsum())

